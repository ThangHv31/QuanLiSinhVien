package SubjectInTable;

public class SubjectInT {
		private int id;
		private double mark;
		public SubjectInT(int id, double mark) {
			super();
			this.id = id;
			this.mark = mark;
		}
		public SubjectInT() {
			super();
		}
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public double getMark() {
			return mark;
		}
		public void setMark(double mark) {
			this.mark = mark;
		}
		
}
